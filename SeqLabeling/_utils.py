import time


def timeit(method):
    def timed(*args, **kw):
        ts = time.time()
        result = method(*args, **kw)
        te = time.time()

        print('%r (%r, %r) %2.2f sec' % (method.__name__, args, kw, te - ts))
        return result

    return timed


def days_hours_mins_secs(td):
    return "{}d,{}h,{}m,{}s".format(td.days, td.seconds // 3600, (td.seconds // 60) % 60, td.seconds % 60)
